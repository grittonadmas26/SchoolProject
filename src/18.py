def add_numbers(a, b):
    """
    Adds two numbers and returns their sum.
    
    Parameters:
    a (int): The first number.
    b (int): The second number.
    
    Returns:
    int: The sum of the two numbers.
    """
    return a + b

# Example usage
result = add_numbers(5, 3)
print(result)  # Output: 8
